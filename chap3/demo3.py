# 整除特殊情况
# 正数整除  2
print(9//4)
# 负数整除  2
print(-9//-4)
# 一正一负  -3  一正一负向下取整
print(-9//4)
print(9//-4)


# 取余特殊情况
# 一正一负  -3  3  一正一负要遵循公式:余数=被除数-除数*商
# 9-(-4)*(-3)=9-12=-3  (商是向下取整的)
print(9%-4)
# (-9)-4*(-3)=-9+12=3
print(-9%4)
