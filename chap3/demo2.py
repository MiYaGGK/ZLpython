# 算数运算符
# 加法运算
print(1+1,type(1+1))
# 减法运算
print(1-1,type(1-1))
# 乘法运算
print(2*3,type(2*3))
# 除法运算
print(9/3,type(9/3))
# 整除运算（没有四舍五入概念，只取整数）
print(11//2,type(11//2))

# 取余运算（只取余数）
print(11%2,type(11%2))

# 幂运算  5的4次方，5*5*5*5
print(5**4,type(5**4))